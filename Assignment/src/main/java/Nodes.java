package main.java;

public class Nodes {

    int xCardinality;
    int yCardinality;

    public Nodes(int xCardinality, int yCardinality) {
        this.xCardinality = xCardinality;
        this.yCardinality = yCardinality;
    }

    public int getxCardinality() {
        return xCardinality;
    }

    public void setxCardinality(int xCardinality) {
        this.xCardinality = xCardinality;
    }

    public int getyCardinality() {
        return yCardinality;
    }

    public void setyCardinality(int yCardinality) {
        this.yCardinality = yCardinality;
    }
}
